// declarating num1 with the value of '3'
int num1 = 3;
// declarating num2 with the value of num1,
// and then iterating num1 (num1 + 1)
int num2 = num1++;
Console.WriteLine($"num1 is {num1}, num2 is {num2}");

// declarating num1 with the value of '3'
int num3 = 3;
// declarating num4 with the value of num3 (num3 + 1)

int num4 = ++num3;
Console.WriteLine($"num3 is {num3}, num4 is {num4}");